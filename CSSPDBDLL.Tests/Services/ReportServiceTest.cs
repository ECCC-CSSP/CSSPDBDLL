        [TestMethod]
        public void ReportService_Constructor_Test()
        {
            foreach (CultureInfo culture in setupData.cultureListGood)
            {
                SetupTest(contactModelListGood[0], culture);

                Assert.IsNotNull(reportService);
                Assert.IsNotNull(reportService.db);
                Assert.IsNotNull(reportService.LanguageRequest);
                Assert.IsNotNull(reportService.User);
                Assert.AreEqual((culture.TwoLetterISOLanguageName == "fr" ? LanguageEnum.fr : LanguageEnum.en), reportService.LanguageRequest);
